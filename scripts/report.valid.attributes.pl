#!/usr/bin/env perl

use strict;
use warnings;

use GraphViz2;

use Log::Handler;

# ---------------

my($logger) = Log::Handler -> new;

$logger -> add
	(
	 screen =>
	 {
		 maxlevel       => 'debug',
		 message_layout => '%m',
		 minlevel       => 'error',
	 }
	);

GraphViz2 -> new(logger  => $logger) -> report_valid_attributes;
